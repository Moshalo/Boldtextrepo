class weapon:
    def __init__(self, name, type, damage, accuracy, bp, sp, color, style, lvl, id):
        self.name = name
        self.type = type
        self.damage = damage
        self.accuracy = accuracy
        self.bp = bp
        self.sp = sp
        self.color = color
        self.style = style
        self.lvl = lvl
        self.id = id